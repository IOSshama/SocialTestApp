    ]
    
    private init() {}
    
    func localizePost(_ post: Post) -> Post {
        let titleIndex = abs(Int(post.id) % russianTitles.count)
        let bodyIndex = abs(Int(post.id) % russianBodies.count)
        
        return Post(
            id: post.id,
            userId: post.userId,
            title: russianTitles[titleIndex],
            body: russianBodies[bodyIndex]
        )
    }
} 