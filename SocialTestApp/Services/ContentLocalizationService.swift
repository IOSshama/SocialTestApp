    private let russianBodies = [
        "Решил поделиться своими впечатлениями о прошедших выходных. Было много интересного и захватывающего. Погода радовала, а компания друзей сделала эти дни незабываемыми.",
        "Сегодня хочу рассказать о своих любимых местах в столице. Каждый уголок города хранит свою историю и особую атмосферу. Особенно люблю старые улочки и уютные кафе.",
        "Программирование стало важной частью моей жизни. Каждый день узнаю что-то новое и развиваюсь в этом направлении. Хочу поделиться своим опытом с начинающими.",
